// switch statements

const grade = 'D';
 
switch (grade) {
    case 'A':
        console.log('you passed');
        break;
    case 'B':
        console.log('you passed');
        break;
    case 'C':
        console.log('you passed');
        break;
    case 'D':
        console.log('you failed');
        break;
    case 'F':
        console.log('you failed');
        break;
    default:
        console.log('you didn\'t even register');
        break;
}